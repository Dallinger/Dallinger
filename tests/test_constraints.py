def test_get_explicit_dallinger_github_requirement(in_tempdir):
    commit_hash = "2a4290c110e72733f07bae2a26b4ff21bc680bb9"

    # TOML with double quotes
    with open("pyproject.toml", "w") as f:
        f.write("[project]\n")
        f.write("dependencies = [\n")
        f.write(
            f'    "dallinger[docker] @ git+https://github.com/Dallinger/Dallinger.git@{commit_hash}",\n'
        )
        f.write("]\n")

    result = _get_explicit_dallinger_github_requirement(Path("pyproject.toml"))
    assert result == commit_hash, f"Expected {commit_hash}, got {result}"

    # TOML with single quotes
    with open("pyproject.toml", "w") as f:
        f.write("[project]\n")
        f.write("dependencies = [\n")
        f.write(
            f"    'dallinger[docker] @ git+https://github.com/Dallinger/Dallinger.git@{commit_hash}',\n"
        )
        f.write("]\n")

    result = _get_explicit_dallinger_github_requirement(Path("pyproject.toml"))
    assert result == commit_hash, f"Expected {commit_hash}, got {result}"

    # requirements.txt (no quotes)
    with open("requirements.txt", "w") as f:
        f.write(
            f"dallinger@git+https://github.com/Dallinger/Dallinger.git@{commit_hash}#egg=dallinger\n"
        )

    result = _get_explicit_dallinger_github_requirement(Path("requirements.txt"))
    assert result == commit_hash, f"Expected {commit_hash}, got {result}"


def test_extras_influence_signature(in_tempdir):
    """Extras change the constraints signature (md5), so different extras produce different constraints."""
    Path("pyproject.toml").write_text(
        "[project]\nname = 'test'\ndependencies = ['dallinger==11.4.0']\n"
        "[project.optional-dependencies]\ndocker = ['docker']\ntest = ['pytest']\n"
    )
    Path(".python-version").write_text("3.12.0")
    sig_none = _get_constraints_signature(Path("pyproject.toml"), extras=[])
    sig_docker = _get_constraints_signature(Path("pyproject.toml"), extras=["docker"])
    sig_test = _get_constraints_signature(Path("pyproject.toml"), extras=["test"])
    sig_both = _get_constraints_signature(
        Path("pyproject.toml"), extras=["docker", "test"]
    )
    assert sig_none != sig_docker
    assert sig_docker != sig_test
    assert sig_both != sig_docker
    assert sig_both != sig_test
    # Order should not matter
    assert (
        _get_constraints_signature(Path("pyproject.toml"), extras=["test", "docker"])
        == sig_both
    )


@pytest.mark.slow
def test_extras_passed_to_resolver(in_tempdir, python_version_file):
    """Resolver (uv pip compile) receives --extra when extras are provided."""
    Path("pyproject.toml").write_text(
        "[project]\nname = 'test'\ndependencies = ['dallinger==11.4.0']\n"
        "[project.optional-dependencies]\ndocker = ['docker']\n"
    )

    def mock_side_effect(cmd, **kwargs):
        out_idx = cmd.index("--output-file") + 1
        Path(cmd[out_idx]).write_text("# autogenerated\nflask==3.1.1\n")
        return b""

    with patch("dallinger.constraints.subprocess.check_output") as mock_check:
        mock_check.side_effect = mock_side_effect
        with patch("dallinger.constraints.uv_available", return_value=True):
            generate_constraints(extras=["docker"])
        call_args = mock_check.call_args[0][0]
        assert "--extra" in call_args
        extra_idx = call_args.index("--extra")
        assert call_args[extra_idx + 1] == "docker"


