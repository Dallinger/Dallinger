@pytest.mark.slow
def test_constraints_cli_allows_extras_after_subcommand(
    in_tempdir, python_version_file
):
    Path("pyproject.toml").write_text(
        "[project]\nname = 'test'\ndependencies = ['dallinger==11.4.0']\n"
        "[project.optional-dependencies]\ndev = ['pytest']\n"
    )

    def mock_side_effect(cmd, **kwargs):
        out_idx = cmd.index("--output-file") + 1
        Path(cmd[out_idx]).write_text("# autogenerated\nflask==3.1.1\n")
        return b""

    runner = CliRunner()
    with patch("dallinger.constraints.subprocess.check_output") as mock_check:
        mock_check.side_effect = mock_side_effect
        with patch("dallinger.constraints.uv_available", return_value=True):
            result = runner.invoke(constraints_cli, ["generate", "--e", "dev"])

    assert result.exit_code == 0, result.output
    call_args = mock_check.call_args[0][0]
    assert "--extra" in call_args
